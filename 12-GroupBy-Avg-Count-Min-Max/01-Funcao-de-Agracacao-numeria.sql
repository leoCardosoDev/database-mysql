-- FUNCAO TRUNCATE
SELECT MAX(JANEIRO) AS MAX_JAN,
MIN(JANEIRO) AS MIN_JAN,
TRUNCATE(AVG(JANEIRO), 2) AS MEDIA_JAN FROM VENDEDORES;